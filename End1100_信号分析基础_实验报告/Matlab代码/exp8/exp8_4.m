N=37;  
wp=0.8*pi; 
ws=0.6*pi;  
wc =(wp+ws)/2;  
N1=fix(wc/(2*pi/N)); 
N2=N-2*N1-1;  
A=[zeros(1,N1+1),ones(1,N2),zeros(1,N1)];  
A(N1+2)=0.1095; 
A(N-N1)=0.598;  
thta=-pi*[0:N-1]*(N-1)/N; 
H=A.*exp(j*thta);  
h=real(ifft(H)); 
v=1:N;  
subplot(2,2,1), 
plot(v,A,'k*'); 
title('频率样本'); 
ylabel('H(k)');  
axis([0,fix(N*1.1),-0.1,1.1]); 
subplot(2,2,2), 
stem(v ,h,'k'); 
title('脉冲响应'); 
ylabel('h(n)');  
axis([0,fix(N*1.1),min(h)*1.1,max(h)*1.1]); 
M=500; 
n1=[1:N];  
w=linspace(0,pi,M); 
X=h*exp(-j*n1'*w); 
subplot(2,2,3);  
plot(w./pi,abs(X),'k'); 
xlabel('\omega/\pi'); 
ylabel('Hd(w)');  
title('幅度响应');axis([0,1,-0.1,1.3]); 
subplot(2,2,4);  
plot(w./pi,20*log10(abs(X)),'k'); 
title('幅度响应'), 
xlabel('\omega/\pi'); 
ylabel('dB');  
axis([0,1,-80,10]);