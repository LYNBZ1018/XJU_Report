t=0:0.001:1; 
%采样周期为 0.001s,即采样频率为 1000Hz; 
%产生受噪声污染的正正弦波信号； 
x=sin(2*pi*100*t)+sin(2*pi*200*t)+rand(size(t)); 
subplot(2,1,1) 
plot(x(1:100)); title('含噪原始信号')
%画出时域内的信号； 
Y=fft(x,512); 
%对 X 进行 512 点的傅立叶变换； 
f=1000*(0:256)/512; 
%设置频率轴（横轴）坐标，1000 为采样频率； 
subplot(2,1,2) 
plot(f,abs(Y(1:257))); title('含噪信号FFT频谱')